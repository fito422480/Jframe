    private void btn_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_grabarActionPerformed
    if (txt_codigo.getText().trim().isEmpty()){
    JOptionPane.showMessageDialog(null, "Debe ingresar el Código", "Error", JOptionPane.ERROR_MESSAGE);
    txt_codigo.requestFocus();
    return;
       }
    
    if (txt_nombre.getText().trim().isEmpty()){
    JOptionPane.showMessageDialog(null, "Debe ingresar el Nombre", "Error", JOptionPane.ERROR_MESSAGE);
    txt_nombre.requestFocus();
    return;
       }
    
    if (txt_apellido.getText().trim().isEmpty()){
    JOptionPane.showMessageDialog(null, "Debe ingresar el Apellido", "Error", JOptionPane.ERROR_MESSAGE);
    txt_apellido.requestFocus();
    return;
       }
    if (txt_cedula.getText().trim().isEmpty()){
    JOptionPane.showMessageDialog(null, "Debe ingresar la Cédula", "Error", JOptionPane.ERROR_MESSAGE);
    txt_cedula.requestFocus();
    return;
       }
    if (txt_direccion.getText().trim().isEmpty()){
    JOptionPane.showMessageDialog(null, "Debe ingresar la Dirección", "Error", JOptionPane.ERROR_MESSAGE);
    txt_direccion.requestFocus();
    return;
       }
    if (txt_telefono.getText().trim().isEmpty()){
    JOptionPane.showMessageDialog(null, "Debe ingresar el Teléfono", "Error", JOptionPane.ERROR_MESSAGE);
    txt_telefono.requestFocus();
    return;
       }
    int mensaje = JOptionPane.showConfirmDialog(null, "¿Desea Grabar?", "Atención", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
    if (mensaje == 0){
      DefaultTableModel tabla = (DefaultTableModel) tabla_contenido.getModel();
      if (variable_accion.equals("Nuevo")){
           tabla.addRow (new Object []{
           txt_codigo.getText(),
           txt_nombre.getText().toUpperCase(),
           txt_apellido.getText().toUpperCase(),
           txt_cedula.getText(),
           txt_direccion.getText().toUpperCase(),
           txt_telefono.getText(),
           cbox_ciudad.getSelectedItem(),
           cbox_pais.getSelectedItem(),
           rabtn_estado.isSelected()  
       });
      }
      

      if (variable_accion.equals("modificar")){
          tabla.removeRow(tabla_contenido.getSelectedRow());
           tabla.addRow (new Object []{
           txt_codigo.getText(),
           txt_nombre.getText().toUpperCase(),
           txt_apellido.getText().toUpperCase(),
           txt_cedula.getText(),
           txt_direccion.getText().toUpperCase(),
           txt_telefono.getText(),
           cbox_ciudad.getSelectedItem(),
           cbox_pais.getSelectedItem(),
           rabtn_estado.isSelected()  
       });
     }
    
      
    }
   
    
    }//GEN-LAST:event_btn_grabarActionPerformed

