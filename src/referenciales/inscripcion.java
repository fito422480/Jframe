    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
   
        if(txt_codigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"DEBE SELECCIONAR UN REGISTRO","ATENCION!!!",JOptionPane.WARNING_MESSAGE);
            return;
        }
        int mensaje = JOptionPane.showConfirmDialog(null,"¿DESEA ELIMINAR EL REGISTRO SELECCIONADO?", "ATENCION!!!", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (mensaje==0){
            DefaultTableModel tab = (DefaultTableModel)tabla_contenido.getModel();
                    tab.removeRow(tabla_contenido.getSelectedRow());
            btn_cancelar.doClick();
        }

    }//GEN-LAST:event_btn_eliminarActionPerformed

