    private void clear_txt() {
    this.txt_pass.setText("");
    }

    private void deshabilitar_txt() {
    this.txt_pass.setEnabled(false);
    }

    private void deshabilitar_botones() {
    this.btn_acceder.setEnabled(false);
    this.btn_salir.setEnabled(false);
    this.btn_cancelar.setEnabled(true);
    }

    private void habilitar_botones() {
    this.btn_acceder.setEnabled(true);
    this.btn_salir.setEnabled(true);
    this.btn_cancelar.setEnabled(false);  
        
    }

    private void habilitar_txt() {
    this.txt_pass.setEnabled(true);
        
    }
