    private void btn_accederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accederActionPerformed
    String usuario, password;
    usuario = txt_user.getText();
    password = txt_pass.getText();
    
    if ("admin".equals(usuario)&& "2022".equals(password)){
     JOptionPane.showMessageDialog(null, "Bienvenido al Sistema", "Datos correctos",
             JOptionPane.INFORMATION_MESSAGE);
     new menu_principal().setVisible(true);
     this.setVisible(false);
     
        }else{
     JOptionPane.showMessageDialog(null, "Acceso denegado", "Ingrese los datos correctos",
             JOptionPane.ERROR_MESSAGE);   
    }
    
    }//GEN-LAST:event_btn_accederActionPerformed

